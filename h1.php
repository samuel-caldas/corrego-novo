  <h1>HISTÓRIA DO  MUNICÍPIO</h1>
  
  
  <p>&nbsp;</p>
  <p><strong>O Povoado</strong></p>
  <p>O  Município mineiro &ldquo;Córrego Novo&rdquo; deve sua formação à coragem de homens como  Antônio Albano dos Reis e Cesário Albano dos Reis, que vieram do povoado de  Iguaçu e ocuparam uma Sesmaria às margens de um curso d&rsquo;água por eles  denominado  &ldquo;Córrego Novo&rdquo;. <br>
    E  com o tempo, pessoas que vinham da redondeza foram se instalando em locais  próximos desse córrego, que mais tarde se deu a formação do município.<br>
    Estes  primeiros povoadores ocuparam as proximidades da então conhecida; praça Santa  Efigênia, que onde mais tarde se deu a construção da primeira capela a &ldquo;Capela  de Santa Efigênia&rdquo;. Com o tempo o município foi se expandindo, ganhando novas  ruas, vilas e bairros. <br>
    Onde  hoje é localizado o centro da cidade, possui umas das mais belas igrejas do  município, a &ldquo;Igreja Sagrada Família&rdquo;; de arquitetura arredondada,  que se destaca no alto de um morro, bem no  centro da cidade. Esta teve sua construção iniciada por volta da década de  70,  depois teve sua obra reiniciada na  direção do pároco Padre Aníbal Borges e finalizada na direção do pároco padre  Léssio Guedes.<br>
    O Bairro São Geraldo, se formou com a migração  de pessoas que vieram do acampamento que existia nas redondezas da &ldquo;Lagoa das  Piabas&rdquo;, de trabalhadores da Empresa Acesita Energética.<br>
    No  final da década de 80 a  cidade foi contemplada também com a construção no ano de 2005, com grande  esforço da paróquia e doações da comunidade, de uma igreja; a de &ldquo;São Geraldo&rdquo;,  onde os fiéis se reúnem uma vez por semana na celebração da santa missa.<br>
    Demorou  algum tempo para formar o povoado que ficou pertencente ao município de Bom  Jesus do galho.  E mais tarde no dia  vinte e sete de julho de um mil e novecentos e quarenta e oito esse povoado se  tornou Distrito, devido ao grande esforço dos políticos da época, em terras do  Município de Bom Jesus do Galho.</p>
  <p>&nbsp;</p>
  <p><strong>Por fim, Cidade.</strong></p>
  <p>O  Distrito foi criado pela Lei nº 336 de 27 de julho de 1.948, sob a  administração do Município de Bom Jesus do Galho. A Lei nº 2.764 de <strong>30 de dezembro de 1.962</strong> desmembrou seu  território do citado Município e emancipou-o politicamente. Foi designado  intendente do Novo Município que assim surgiu, o senhor Waldomiro Cupertino de  Lima, substituído em seguida pelo primeiro Prefeito eleito, senhor Alberto  Vieira de Abreu.<br>
    Francisco  Caetano de Souza Filho, primeiro Escrivão de Paz e Notas, foi o principal  articulador da emancipação política da localidade. Era Prefeito de Bom Jesus do  Galho e sua intervenção foi decisiva para a formação administrativa de Córrego  Novo. </p>
  <p>&nbsp;</p>
  <p><strong>A instalação do Município</strong> <br>
    No dia 1º de Março de 1963, foi instalado o  Município de Córrego Novo em sessão solene no edifício alugado para o  funcionamento da prefeitura sobre a presidência do Srº Valdomiro Cupertino de  Lima, que foi nomeado intendente pelo prefeito de Bom Jesus do Galho e teve  também a  presença marcante de autoridade  e populares.</p>
  <p><strong>Os Discursantes</strong><br>
    Discursaram  na ocasião os senhores: Jose Manoel Dornelas (Vereador de Bom Jesus do Galho);  Francisco Caetano de Souza Filho (Prefeito Municipal de Bom Jesus do Galho) e  Geraldo Moraes Quintão (Deputado Estadual).<br>
    O  intendente Valdomiro Cupertino de Lima permaneceu no cargo durante 6 (seis)  meses. Durante sua gestão providenciou a eleição para prefeito, vice-prefeito e  vereadores à Câmara Municipal.</p>
  <p>&nbsp;</p>
  <p><strong>A Primeira Eleição</strong><br>
    O  primeiro pleito foi realizado no dia 30 de junho de 1963, com o primeiro  Prefeito Alberto Vieira de Abreu e seu vice Cassiano Lana Forneas e seus nove  vereadores.<br>
    Tomaram  posse no dia 30 de Agosto de 1963 ficando no poder a partir do dia 30 de agosto  de 1963 a  30 de janeiro de 1967.<br>
    Dando  continuidade ao desenvolvimento do município vieram os demais prefeitos.<br>
    A  2ª prefeita de Córrego Novo Srª Sebastiana Rosa de Souza teve como prioridade a  Educação, construindo 6 (seis) escolas municipais, na zona rural. <br>
    Com  o passar do tempo às dificuldades foram aparecendo no campo, acontecendo um  grande numero de emigração. Assim as escolas construídas foram sendo  desativadas, restando apenas 2 (duas) escolas municipais. E uma estadual.<br>
    A  administração atual tem como prefeito o Sr. Eder Fragoso de Souza e o  Vice-Prefeito o Sr. Dalton Caetano   Campos. </p>
  <p>  
  <p><strong>Características do Município</strong><br>
  <p>O  Município se localiza na  região do Rio  Doce, limita-se ao Norte com Pingo D`água, ao Sul com Raul Soares, a Leste com  Bom Jesus do Galho e a Oeste com   Marliéria.<br>
    A  extensão total de seu território ocupa uma área de 198 Km² (cento e noventa e  oito quilômetros quadrados), situando-se a uma altitude máxima 854 metros e mínima de 262 metros acima do  nível do Mar.<br>
    Temperatura  média de 22,7°c. Índice médio pluviométrico 1200 mm, topografia plana  70%; ondulada 20%; montanhosa 10%. Principais rios: rio do Óculo; Córrego do  Mantimento (bacia do Rio Doce).</p>
  <p><strong>Densidade Demográfica</strong><br>
    População  geral estimada de 3.186 habitantes (IBGE – 2007).<br>
    Em  1987, ocorreu o aumento no fluxo populacional do município em virtude da  migração das diversas comunidades rurais vizinhas tendo em vista a proposta  política aos operários pertencentes à Acesita energética que ocasionou o êxodo  rural.</p>
  <p>&nbsp;</p>
  <p><strong>Vegetação</strong><br>
    Formada  pelo domínio da Mata Atlântica, sendo reflorestamento uma atividade que cobre  grande extensão para produção da celulose e carvão, sendo a  vegetação secundaria formada pela capoeira.</p>
  
  <p>&nbsp;</p>
  <p><strong>Transporte</strong><br>
    Principais  rodovias: BR – 381, e BR – 458 e BR 116. Distâncias dos principais centros por  rodovias (Km): Belo Horizonte 319   km, Rio de Janeiro 480 km, São Paulo 790 km, Brasília 1.090 km, Vitória 345 km. Distancias os  Municípios limítrofes e/ou centralizadores de serviços públicos (Km): Caratinga  39 km,  Governador Valadares 154 km,  Coronel Fabriciano 67 km,  Ipatinga 75km, Bom Jesus do Galho 14   km, Raul Soares 42 km, Pingo D`água 18 Km e Timoteo 60 Km.</p>
  <p>&nbsp;</p>
  <p><strong>Suporte Econômico.</strong> <br>
    Situa-se  nas proximidades do Vale do Aço; constituído por: Timóteo, Coronel Fabriciano e  Ipatinga. O suporte econômico de Córrego Novo é a agricultura, com larga  produção de café, milho e feijão. <br>
    A  pecuária é também bastante importante economicamente, registrando-se rebanho de  bovinos e suínos. <br>
    Na  Região há capacitação em pratica de manejo, alimentação, recuperação de  pastagem e instalações para aumentar a oferta de leite no período de seca. </p>
  <p>&nbsp;</p>
  <p><strong>Educação e Cultura</strong></p>
  <br />
  <p>&nbsp;O Município hoje conta com três escolas da rede municipal que possui ensino  fundamental (do pré de 4 anos à quarta série): Escola Municipal Domingos  Pascoal de Paula, localizada na Cabeceira do Mantimento (Zona Rural); Escola  Municipal Dona Camila da Conceição de Paula, localizada no Córrego do  Mantimento (zona rural); Escola Municipal Professor Borges da Costa na zona urbana.  Uma escola da rede estadual com ensino médio, a Escola Estadual Presidente  Tancredo de Almeida Neves e uma Creche Municipal &ldquo;Vovô Gegê&rdquo;.</p>
  <p>&nbsp;Além das tradicionais festas  juninas promovidas pelas escolas, o município realiza outras como: as festas  religiosas, que acontecem todo ano, como por exemplo a de Santa Efigênia, em  homenagem a padroeira da cidade.<br>
    A  mais tradicional festa do município é a Cavalgada. É uma festa regional,  mostrando os belos animais que os pequenos fazendeiros possuem além da presença  de bandas regionais e local. Equipes de rodeios, concursos para eleger a rainha  da festa, concurso leiteiro, o tradicional &ldquo;Encontro de Violeiros&rdquo; e concurso  de marcha em várias categorias.<br>
   &nbsp; A primeira cavalgada aconteceu em 1991, com a  presença e participação de todos os municípios vizinhos e também dos  Corregonovenses ausentes, a festa tornou-se uma festa tradicional, mostrando um  pouco de nossa cultura.</p>
